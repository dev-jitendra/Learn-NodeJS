// Break Keyword
for(let i=0; i<=10; i++){
    if(i===4){
        break;
    }
    console.log(`${i}`);
}
console.log(`Hello Break keyword`)


// Continue Keyword
for(let i=0; i<=10; i++){
    if(i===4){
        continue;
    }
    console.log(`${i}`);
}
console.log(`Hello continue keyword`)